function mostrarDesplegable() {
    var desplegable = document.getElementById("desplegable");
    if (desplegable.style.display === "none" || desplegable.style.display === "") {
        desplegable.style.display = "block";
    } else {
        desplegable.style.display = "none";
    }
}
